                <h1>Walk It Out</h1>
                <p>Walk It Out (WIO) held their third annual multicultural fashion show on April 7, 2012, showcasing an array of fashions from diverse cultures across the globe. With over 1,000 people in attendance, fifteen student organizations collaborated to celebrate cultural diversity on the University of Iowa campus. This year, University Hospitals and Clinics HIV/AIDS Program also participated in the fashion show to increase awareness and promote education of HIV and AIDS on campus. Hip-Hop, East Asian, LGBTQ, South Asian, Middle Eastern, African, and Latin American cultures and regions were represented in the show.</p>

                <p>Student President of the Walk It Out Fashion Show, Evan Ivory, said this year’s show has been the best one yet.The 2012 fashion show improved significantly compared to previous years.</p>

                <p>“We learned and went through a lot as an executive board, but we all grew from it, both as a team and as individuals.” Ivory continued, “I think that work carried over into the intense planning for the third annual fashion show.”</p>

                <p>In terms of quality, the music transitions, the clothing the models wore, and even the containment of the 100+ models backstage, every aspect showed improvement from the year before.</p>

                <p>“Most importantly, this year’s show had participants interact more with one another while educating them on unfamiliar topics at the same time,” Ivory said.</p>

                <p>WIO promotes the word “multicultural” juxtapose their fashion show as much as possible to consistently remind the campus what they stand for. Varied advertisements were used to spread cultural awareness across campus representing each culture that would be showcased in the main event.</p>

                <p>The faces of WIO also promoted diversity by demonstrating that a cultured group of students can work and grow together to achieve excellence and higher education, all while enjoying themselves. The fashion show pushed their participants to actively seek out ways to meet people who are different from them. To support this idea, WIO hosted several Diversity Mixers throughout the year. These mixers allowed the models and other participants to do activities together, enjoy a meal together, all while getting to know someone new. Furthermore, participating in the Walk It Out fashion show allowed several students the opportunity to step outside their comfort zone and participate in a character altering event.</p>

                <p>“Walk It Out has continuously pushed it’s participants to actively seek out ways to meet people who are different from them,” Ivory said.</p>
