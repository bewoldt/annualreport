                            <h2 class="title">Creating Positive Change On Campus</h2>
                        </div>
                        <div class="meta">
                            <ul>
                                <li>IMU Change Team</li>
                                <li><em>Words by <span>Maddy Osman</span></em></li>
                                <li><em>Photo by <span>Chelsea Darter</span></em></li>
