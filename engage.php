                    <p>Pick One is an initiative for first year students to get involved on campus. There are nine areas students can choose from: Academic/Honors Opportunities, Campus Employment, Fraternities and Sororities, Intramural or Club Sports, Leadership, Multicultural Activities, Performing Arts, Student Organizations, and Volunteering. But don’t let that limit you to just one opportunity! The most popular categories are student organizations (there are over 450 on campus!). Close followers are Fraternities and Sororities, Intramural Sports, and Student Employment. The most popular specific Pick One opportunity is Dance Marathon, but HawksNest and Intramural Football/Flag Football also have a lot of student involvement.</p>

                    <p>Pick One is in its 5th year of operation and sees continuous growth and engagement each year. This year, Pick One had a participation rate of more than 42% of all first-year students.</p>

