                            <h2 class="title">Building a Women's Network</h2>
                            <h3 class="sub-head">Iowa N.E.W. Leadership Summer Institute</h3>
                        </div>
                        <div class="meta">
                            <ul>
                                <li>Women's Resource and Action Center</li>
                                <li><em>Words by <span>Mary Koberstein</span></em></li>
